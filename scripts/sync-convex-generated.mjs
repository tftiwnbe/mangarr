import { mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, '..');
const webConvexDir = path.join(repoRoot, 'web', 'src', 'convex');
const sourcePath = path.join(repoRoot, 'web', 'src', 'convex', '_generated', 'api.ts');
const targetPath = path.join(repoRoot, 'worker', 'src', 'generated', 'convex-api.ts');
const modulesDir = path.join(repoRoot, 'worker', 'src', 'generated', 'modules');

const source = await readFile(sourcePath, 'utf8');
const moduleMatches = [...source.matchAll(/import type \* as (\w+) from "\.\.\/([^"]+)\.js";/g)];
const moduleNames = moduleMatches.map((match) => match[2]);

await rm(modulesDir, { recursive: true, force: true });
await mkdir(modulesDir, { recursive: true });

for (const moduleName of moduleNames) {
	const moduleSourcePath = path.join(webConvexDir, `${moduleName}.ts`);
	let sourceText = '';
	try {
		sourceText = await readFile(moduleSourcePath, 'utf8');
	} catch {
		sourceText = '';
	}
	const exportedFunctions = [...sourceText.matchAll(
		/export const\s+([A-Za-z0-9_]+)\s*=\s*(query|mutation|action|internalQuery|internalMutation|internalAction)\s*\(/g
	)].map((match) => ({ name: match[1], kind: match[2] }));

	const imports = new Set();
	const declarations = [];
	for (const item of exportedFunctions) {
		if (item.kind === 'query') {
			imports.add('RegisteredQuery');
			declarations.push(`export const ${item.name}: RegisteredQuery<"public", any, any>;`);
			continue;
		}
		if (item.kind === 'mutation') {
			imports.add('RegisteredMutation');
			declarations.push(`export const ${item.name}: RegisteredMutation<"public", any, any>;`);
			continue;
		}
		if (item.kind === 'action') {
			imports.add('RegisteredAction');
			declarations.push(`export const ${item.name}: RegisteredAction<"public", any, any>;`);
			continue;
		}
		if (item.kind === 'internalQuery') {
			imports.add('RegisteredQuery');
			declarations.push(`export const ${item.name}: RegisteredQuery<"internal", any, any>;`);
			continue;
		}
		if (item.kind === 'internalMutation') {
			imports.add('RegisteredMutation');
			declarations.push(`export const ${item.name}: RegisteredMutation<"internal", any, any>;`);
			continue;
		}
		if (item.kind === 'internalAction') {
			imports.add('RegisteredAction');
			declarations.push(`export const ${item.name}: RegisteredAction<"internal", any, any>;`);
		}
	}

	const body = declarations.length > 0
		? [
				`import type { ${[...imports].sort().join(', ')} } from "convex/server";`,
				...declarations
			].join('\n')
		: 'export const __placeholder: never;';
	const modulePath = path.join(modulesDir, `${moduleName}.d.ts`);
	await writeFile(modulePath, `// AUTO-GENERATED by scripts/sync-convex-generated.mjs\n${body}\n`, 'utf8');
}

const rewrittenSource = source.replace(
	/import type \* as (\w+) from "\.\.\/([^"]+)\.js";/g,
	'import type * as $1 from "./modules/$2.js";'
);

const output =
	`// AUTO-GENERATED by scripts/sync-convex-generated.mjs\n` +
	`// Source: web/src/convex/_generated/api.ts\n${rewrittenSource}\n` +
	`export const convexApi = api;\n`;

await writeFile(targetPath, output, 'utf8');
process.stdout.write(`Synced ${path.relative(repoRoot, targetPath)}\n`);
